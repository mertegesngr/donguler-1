#include <stdio.h>  // Standart giriş-çıkış kütüphanesini içerir. printf() fonksiyonunu kullanmamızı sağlar.

int main (){  // Programın ana fonksiyonu, buradan başlar.




    for (int i = 1 ; i<=4; i++){  // for döngüsü: i'nin başlangıç değeri 1, döngü i <= 4 olduğu sürece devam eder, her döngüde i 1 artar.



      
        printf("mert\n") ;  // printf fonksiyonu: "mert" kelimesini ekrana yazdırır, \n yeni bir satıra geçmeyi sağlar.



    }


    return 0;  // main() fonksiyonu 0 döndürür ise , bu programın başarıyla sona erdiği anlamına gelir.
}
